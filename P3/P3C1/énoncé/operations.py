# addition(a,b): qui retourne la somme de a et b
def addition(a, b):
    """Retourne la somme de a et b."""
    return a + b
# multiplication(a,b): qui retourne le produit de deux nombres a et b
def multiplication(a, b):
    """Retourne le produit de a et b."""
    return a * b


